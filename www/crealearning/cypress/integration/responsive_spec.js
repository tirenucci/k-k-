describe('Test de la responsive', function() {
    beforeEach(function() {
        cy.clearCookies()
        cy.clearLocalStorage()
        cy.wait(1000)
        cy.visit('/connection')
        cy.wait(1000)
        cy.get('.input__mail')
            .type('admin@logipro.com')
            .should('have.value', 'admin@logipro.com')
        cy.get('.input__pswd')
            .type('admin')
            .should('have.value', 'admin')
            cy.get('.connect__btn')
            .click({force: true})
        cy.wait(2000)
    })

    it('Responsive d\'un modules', function(){
        cy.viewport(414, 736)
        cy.visit('/training/1')
        cy.wait(1000)
        cy.get('#mobile__logo')
            .should('be.visible')
        cy.get('#mobile-link')
            .should('be.visible')
        cy.get('.swipper')
            .should('exist')
    })
    
    it('Responsive du tableau des langues', function(){
        cy.viewport(414, 736)
        cy.visit('/configLang')
        cy.wait(1000)
        cy.get('.large-view')
            .should('not.be.visible')
        cy.get('.medium-view')
            .should('not.be.visible')
        cy.get('.small-view')
            .should('not.be.visible')
    })

    it('Responsive de l\'espace client', function(){
        cy.viewport(414, 736)
        cy.visit('/customerArea')
        cy.wait(1000)
        cy.get('.welcome-section')
            .should('not.be.visible')
    })
})